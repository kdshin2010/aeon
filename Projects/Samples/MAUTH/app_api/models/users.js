//handles salt and hash
var crypto = require('crypto');


//JSON web token
//exposes sign method we create a JWT passing data we want to include in token 
//as well as a secret the hashing algorithm uses
//data sent as JS object and shoudl include exp property
var jwt = require('jsonwebtoken')


var userSchema = new mongoose.Schema({
  email: {
    type: String,
    unique: true,
    required: true
  },
  name: {
    type: String,
    required: true
  },
  hash: String,
  salt: String
});

//saving password as salt and Hash
//setPassword method accepts password parameter
//uses crypto.randoBytes --> salt
// crypto.pbkdf2Sync --> hash


userSchema.methods.setPassword = function(password){
  this.salt = crypto.randomBytes(16).toString('hex');
  this.hash = crypto.pbkdf2Sync(password, this.salt, 1000, 64).toString('hex');
};

//Checking Password


userSchema.methods.validPassword = function(password) {
  var hash = crypto.pbkdf2Sync(password, this.salt, 1000, 64).toString('hex');
  return this.hash === hash;
};

//JWT Web token
userSchema.methods.generateJwt = function() {
  var expiry = new Date();
  expiry.setDate(expiry.getDate() + 7);

  return jwt.sign({
    _id: this._id,
    email: this.email,
    name: this.name,
    exp: parseInt(expiry.getTime() / 1000),
  }, "MY_SECRET"); // DO NOT KEEP YOUR SECRET IN THE CODE!
};